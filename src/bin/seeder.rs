fn random_name() -> String {
    let mut rng = rand::rng();
    let first = FIRST_NAMES.choose(&mut rng).unwrap();
    let last = LAST_NAMES.choose(&mut rng).unwrap();
    format!("{first} {last}")
}

fn random_message(i: usize) -> &'static str {
    SAMPLE_MESSAGES[i % SAMPLE_MESSAGES.len()]
}

fn random_likes(i: usize) -> i32 {
    // Spread likes across 0–49, deterministically based on index.
    ((i * 17 + 3) % 50) as i32
}

#[tokio::main]
async fn main() -> anyhow::Result<()> {
    dotenv().ok();

    tracing_subscriber::fmt()
        .with_max_level(tracing::Level::INFO)
        .init();

    let database_url = env::var("DATABASE_URL").expect("DATABASE_URL must be set");
    let db: DatabaseConnection = database::connect(&database_url).await?;

    // Delete all existing rows
    use portfolio_server::entity::wall_message::Entity as WallMessage;
    WallMessage::delete_many().exec(&db).await?;
    println!("Cleared existing messages");

    // Insert 100 fake messages.
    for i in 0..100 {
        // Small sleep so names differ
        std::thread::sleep(std::time::Duration::from_millis(5));

        let msg = ActiveModel {
            name: Set(random_name()),
            message: Set(random_message(i).to_owned()),
            likes: Set(random_likes(i)),
            ..Default::default()
        };

        msg.insert(&db).await?;
        println!("Seeded message {}", i + 1);
    }

    println!("Done seeding 100 messages");
    Ok(())
}
